####################################################################################################
#' makeBoxPlot -- creates boxplot of contigs included in the analysis vs those excluded
#' @param chrTable  A data.frame consisting of chromosomes and lengths. Generated by makeChrTable(). Note rownames equal to chromosome names are required
#' @param linkage.contigs A list of clustered contigs, generated by clusterContigs()
#' @param sex.contigs vector of contigs that appear to be monosomic (ie belonging to sex chromosomes)
#' @return an .svg file to the destination in saveLocation
#'
#' @export
#'
####################################################################################################


makeBoxPlot <- function(chrTable, linkage.contigs, sex.contigs=NULL)
{
	#Save a boxplot
	if(ncol(chrTable) > 2)
	{
		suppressWarnings(chrTable$length <- apply(chrTable, 1, function(x){as.numeric(x[3]) - as.numeric(x[2])}))
	}

	includeLength <- chrTable[which(rownames(chrTable) %in% unlist(linkage.contigs)),'length']/10^6

	if(!is.null(sex.contigs))
	{
	  sexLength <- chrTable[which(rownames(chrTable) %in% unlist(sex.contigs)),'length']/10^6
	  includeLength <- c(includeLength, sexLength)
	}

	excludeLength <- chrTable[!(rownames(chrTable) %in% unlist(linkage.contigs)),'length']/10^6

	if(!is.null(sex.contigs))
	{
	  seXclude <- chrTable[!(rownames(chrTable) %in% unlist(sex.contigs)),'length']/10^6
	  excludeLength <- c(excludeLength, seXclude)
	}
 	sumInclude <- round(sum(includeLength), digits=1)
	sumExclude <- round(sum(excludeLength), digits=1)
	percentInclude <- round(sumInclude / (sumInclude+sumExclude) *100, digits=1)

	boxplot(includeLength, 
	excludeLength, 
	names=c(paste(sumInclude, ' Mb included (', percentInclude, '%)', sep="" ), paste(sumExclude, ' Mb excluded (', 100 - percentInclude, '%)', sep="") ),
	main='Length of contigs included in assembly vs excluded',
	ylab='Contig length (MB)')

}